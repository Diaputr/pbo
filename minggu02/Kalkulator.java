package minggu02;
public class Kalkulator {
    int operan1, operan2;
    float hasil;
    
    void tambah(){
        hasil = operan1+operan2;
    }
    
    void kurang(){
        hasil = operan1-operan2;
    }
    
    void kali(){
        hasil = operan1*operan2;
    }
    
    void bagi(){
        hasil = operan1/operan2;
    }
}